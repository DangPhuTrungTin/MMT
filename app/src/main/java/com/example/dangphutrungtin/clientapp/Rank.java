package com.example.dangphutrungtin.clientapp;

public class Rank {
    private String name;
    private String score;

    public Rank(String name, String score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public String getScore() {
        return score;
    }
}
